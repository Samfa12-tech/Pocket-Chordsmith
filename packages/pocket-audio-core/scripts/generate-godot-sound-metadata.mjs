function gdArray(values) {
  return `[${values.map(gdString).join(", ")}]`;
}

function gdString(value) {
  return JSON.stringify(String(value));
}

function gdValue(value) {
  if (Array.isArray(value)) return `[${value.map(gdValue).join(", ")}]`;
  if (value && typeof value === "object") {
    return `{${Object.entries(value).map(([key, entry]) => `${gdString(key)}: ${gdValue(entry)}`).join(", ")}}`;
  }
  if (typeof value === "boolean") return value ? "true" : "false";
  if (typeof value === "number") return Number.isInteger(value) ? String(value) : String(value);
  return gdString(value);
}

