import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import {
  DEFAULT_CLASSIC_DRUM_KIT,
  DEFAULT_LOFI_DRUM_KIT,
  POCKET_BASS_TONE_CONFIGS,
  POCKET_DRUM_KIT_CONFIGS
} from "../../../packages/pocket-audio-core/src/sounds/lofi-registry.js";
import {
  DEFAULT_CHORD_INSTRUMENT,
  DEFAULT_MELODY_INSTRUMENT,
  POCKET_CHORD_INSTRUMENT_CONFIGS,
  POCKET_LEAD_INSTRUMENT_CONFIGS,
  pocketLeadExtraLayers
} from "../../../packages/pocket-audio-core/src/sounds/instruments.js";
import {
  DEFAULT_GUITAR_TONE,
  POCKET_GUITAR_TONE_CONFIGS
} from "../../../packages/pocket-audio-core/src/sounds/guitar.js";
import { POCKET_PRO_EQ_BANDS } from "../../../packages/pocket-audio-core/src/fx/pro-eq.js";

const outUrl = new URL("../src-tauri/src/generated_sound_recipes.rs", import.meta.url);
const checkOnly = process.argv.includes("--check");
const source = renderRust();

if (checkOnly) {
  const existing = readFileSync(outUrl, "utf8");
  if (existing !== source) {
    console.error(`${fileURLToPath(outUrl)} is stale. Run npm run generate:native-sound-recipes from apps/pocket-daw.`);
    process.exit(1);
  }
  console.log("Native sound recipes are up to date.");
} else {
  writeFileSync(outUrl, source);
  console.log(`Wrote ${fileURLToPath(outUrl)}`);
}

function renderRust() {
  const drumIds = Object.keys(POCKET_DRUM_KIT_CONFIGS);
  const bassIds = Object.keys(POCKET_BASS_TONE_CONFIGS);
  const chordIds = Object.keys(POCKET_CHORD_INSTRUMENT_CONFIGS);
  const leadIds = Object.keys(POCKET_LEAD_INSTRUMENT_CONFIGS);
  const guitarIds = Object.keys(POCKET_GUITAR_TONE_CONFIGS);
  return `${header()}
fn parametric_eq_filters(params: &std::collections::HashMap<String, serde_json::Value>, sample_rate: f32) -> Vec<BiquadFilter> {
    let mut filters = Vec::new();
${POCKET_PRO_EQ_BANDS.map(renderProEqBand).join("\n")}
    filters
}

fn native_drum_kit_config(kit: &str) -> NativeDrumKitConfig {
    match kit {
${drumIds.filter((id) => id !== "classic").map((id) => `        "${id}" => native_drum_kit_${rustIdent(id)}(),`).join("\n")}
        _ => native_drum_kit_classic(),
    }
}

fn generated_native_resolve_drum_kit(kit: Option<&str>, audio_profile: Option<&str>, lofi_preset: Option<&str>) -> &'static str {
    match kit.unwrap_or("") {
${drumIds.map((id) => `        "${id}" => "${id}",`).join("\n")}
        _ if generated_native_lofi_active(audio_profile, lofi_preset) => "${DEFAULT_LOFI_DRUM_KIT}",
        _ => "${DEFAULT_CLASSIC_DRUM_KIT}",
    }
}

fn generated_native_lofi_active(audio_profile: Option<&str>, lofi_preset: Option<&str>) -> bool {
    audio_profile == Some("lofi_chill") || lofi_preset.unwrap_or("").starts_with("lofi_")
}

${drumIds.map((id) => renderDrumKit(id, POCKET_DRUM_KIT_CONFIGS[id])).join("\n\n")}

fn native_bass_tone_config(tone: Option<&str>) -> NativeBassToneConfig {
    match tone.unwrap_or("classic") {
${bassIds.filter((id) => id !== "classic").map((id) => `        "${id}" => native_bass_tone_${rustIdent(id)}(),`).join("\n")}
        _ => native_bass_tone_classic(),
    }
}

${bassIds.map((id) => renderBassTone(id, POCKET_BASS_TONE_CONFIGS[id])).join("\n\n")}

fn generated_native_chord_config(instrument: Option<&str>) -> NativeChordConfig {
    match instrument.unwrap_or("${DEFAULT_CHORD_INSTRUMENT}") {
${chordIds.filter((id) => id !== DEFAULT_CHORD_INSTRUMENT).map((id) => `        "${id}" => generated_native_chord_${rustIdent(id)}(),`).join("\n")}
        _ => generated_native_chord_${rustIdent(DEFAULT_CHORD_INSTRUMENT)}(),
    }
}

${chordIds.map((id) => renderChordLayers(id, POCKET_CHORD_INSTRUMENT_CONFIGS[id])).join("\n\n")}

${chordIds.map((id) => renderChordInstrument(id, POCKET_CHORD_INSTRUMENT_CONFIGS[id])).join("\n\n")}

fn generated_native_lead_config(instrument: Option<&str>) -> NativeLeadConfig {
    match instrument.unwrap_or("${DEFAULT_MELODY_INSTRUMENT}") {
${leadIds.filter((id) => id !== DEFAULT_MELODY_INSTRUMENT).map((id) => `        "${id}" => generated_native_lead_${rustIdent(id)}(),`).join("\n")}
        _ => generated_native_lead_${rustIdent(DEFAULT_MELODY_INSTRUMENT)}(),
    }
}

${leadIds.map((id) => renderLeadExtras(id, POCKET_LEAD_INSTRUMENT_CONFIGS[id])).join("\n\n")}

${leadIds.map((id) => renderLeadInstrument(id, POCKET_LEAD_INSTRUMENT_CONFIGS[id])).join("\n\n")}

fn generated_guitar_tone_config(tone: Option<&str>) -> NativeGuitarToneConfig {
    match tone.unwrap_or("${DEFAULT_GUITAR_TONE}") {
${guitarIds.filter((id) => id !== DEFAULT_GUITAR_TONE).map((id) => `        "${id}" => generated_guitar_tone_${rustIdent(id)}(),`).join("\n")}
        _ => generated_guitar_tone_${rustIdent(DEFAULT_GUITAR_TONE)}(),
    }
}

${guitarIds.map((id) => renderGuitarTone(id, POCKET_GUITAR_TONE_CONFIGS[id])).join("\n\n")}
`;
}

function header() {
  return `// Generated by apps/pocket-daw/scripts/generate-native-sound-recipes.mjs.
// Source of truth: packages/pocket-audio-core/src/sounds/* and src/fx/pro-eq.js.
// Do not edit by hand.

`;
}

function renderProEqBand(band) {
  const enabled = `bool_param(params, "${band.enabledParam}", ${Boolean(band.defaultEnabled)})`;
  if (band.nodeType === "highpass") {
    return `    if ${enabled} {
        filters.push(BiquadFilter::highpass(
            sample_rate,
            param(params, "${band.frequencyParam}", ${num(band.defaultFrequency)}).clamp(${num(band.minFrequency)}, ${num(band.maxFrequency)}),
            param(params, "${band.qParam}", ${num(band.defaultQ)}).clamp(${num(band.minQ)}, ${num(band.maxQ)}),
        ));
    }`;
  }
  if (band.nodeType === "lowpass") {
    return `    if ${enabled} {
        filters.push(BiquadFilter::lowpass(
            sample_rate,
            param(params, "${band.frequencyParam}", ${num(band.defaultFrequency)}).clamp(${num(band.minFrequency)}, ${num(band.maxFrequency)}),
            param(params, "${band.qParam}", ${num(band.defaultQ)}).clamp(${num(band.minQ)}, ${num(band.maxQ)}),
        ));
    }`;
  }
  if (band.nodeType === "lowshelf" || band.nodeType === "highshelf") {
    return `    if ${enabled} {
        filters.push(BiquadFilter::shelf(
            sample_rate,
            "${band.nodeType}",
            param(params, "${band.frequencyParam}", ${num(band.defaultFrequency)}).clamp(${num(band.minFrequency)}, ${num(band.maxFrequency)}),
            param(params, "${band.gainParam}", ${num(band.defaultGain)}).clamp(${num(band.minGain)}, ${num(band.maxGain)}),
        ));
    }`;
  }
  if (band.nodeType === "peaking") {
    return `    if ${enabled} {
        filters.push(BiquadFilter::peaking(
            sample_rate,
            param(params, "${band.frequencyParam}", ${num(band.defaultFrequency)}).clamp(${num(band.minFrequency)}, ${num(band.maxFrequency)}),
            param(params, "${band.qParam}", ${num(band.defaultQ)}).clamp(${num(band.minQ)}, ${num(band.maxQ)}),
            param(params, "${band.gainParam}", ${num(band.defaultGain)}).clamp(${num(band.minGain)}, ${num(band.maxGain)}),
        ));
    }`;
  }
  throw new Error(`Unsupported Pocket Pro EQ band node type: ${band.nodeType}`);
}

function renderDrumKit(id, kit) {
  return `fn native_drum_kit_${rustIdent(id)}() -> NativeDrumKitConfig {
    NativeDrumKitConfig {
        kick: NativeKickConfig {
            start_freq: ${num(kit.kick.startFreq)},
            end_freq: ${num(kit.kick.endFreq)},
            sweep_seconds: ${num(kit.kick.sweepSeconds)},
            filter_freq: ${optionNum(kit.kick.filterFreq)},
            gain_floor: ${num(kit.kick.gainFloor)},
            gain_scale: ${num(kit.kick.gainScale)},
            length: ${num(kit.kick.length)},
            ramp_seconds: ${num(kit.kick.rampSeconds)},
        },
        snare: NativeSnareConfig {
            noise_seconds: ${num(kit.snare.noiseSeconds)},
            highpass: ${num(kit.snare.highpass)},
            lowpass: ${optionNum(kit.snare.lowpass)},
            gain_floor: ${num(kit.snare.gainFloor)},
            gain_scale: ${num(kit.snare.gainScale)},
            length: ${num(kit.snare.length)},
            ramp_seconds: ${num(kit.snare.rampSeconds)},
            body_freq: ${optionNum(kit.snare.bodyFreq)},
            body_gain: ${num(kit.snare.bodyGain ?? 0)},
            body_length: ${num(kit.snare.bodyLength ?? 0)},
            body_ramp_seconds: ${num(kit.snare.bodyRampSeconds ?? 0)},
        },
        hat: NativeHatConfig {
            closed_length: ${num(kit.hat.closedLength)},
            open_length: ${num(kit.hat.openLength)},
            highpass_closed: ${num(kit.hat.highpassClosed)},
            highpass_open: ${num(kit.hat.highpassOpen)},
            lowpass: ${optionNum(kit.hat.lowpass)},
            gain_floor_closed: ${num(kit.hat.gainFloorClosed)},
            gain_floor_open: ${num(kit.hat.gainFloorOpen)},
            gain_scale_closed: ${num(kit.hat.gainScaleClosed)},
            gain_scale_open: ${num(kit.hat.gainScaleOpen)},
            ramp_seconds_closed: ${num(kit.hat.rampSecondsClosed)},
            ramp_seconds_open: ${num(kit.hat.rampSecondsOpen)},
        },
    }
}`;
}

function renderBassTone(id, tone) {
  return `fn native_bass_tone_${rustIdent(id)}() -> NativeBassToneConfig {
    NativeBassToneConfig {
        main_wave: "${tone.mainWave}",
        sub_wave: "${tone.subWave}",
        main_peak: ${num(tone.mainPeak)},
        sub_peak: ${num(tone.subPeak)},
        cutoff: ${num(tone.cutoff)},
        sub_cutoff: ${num(tone.subCutoff)},
        attack: ${num(tone.attack)},
    }
}`;
}

function renderChordLayers(id, cfg) {
  const layers = Array.isArray(cfg.layers) ? cfg.layers : [];
  return `const GENERATED_CHORD_${rustConstIdent(id)}_LAYERS: [NativeChordLayerConfig; ${layers.length}] = [${layers.length ? `\n${layers.map(renderChordLayer).join(",\n")}\n` : ""}];`;
}

function renderChordLayer(layer) {
  return `    NativeChordLayerConfig {
        wave: "${layer.wave}",
        freq_mul: ${num(layer.freqMul ?? 1)},
        detune: ${num(layer.detune ?? 0)},
        level: ${num(layer.level ?? 1)},
    }`;
}

function renderChordInstrument(id, cfg) {
  return `fn generated_native_chord_${rustIdent(id)}() -> NativeChordConfig {
    NativeChordConfig {
        root_wave: "${cfg.rootWave}",
        wave: "${cfg.wave}",
        peak: ${num(cfg.peak)},
        filter: "${cfg.filter}",
        freq: ${num(cfg.freq)},
        filter_q: ${num(cfg.filterQ)},
        filter_sweep: ${optionNum(cfg.filterSweep)},
        attack: ${num(cfg.attack)},
        decay: ${num(cfg.decay)},
        sustain: ${num(cfg.sustain)},
        release: ${num(cfg.release)},
        dur_mul: ${num(cfg.durMul)},
        spread_mul: ${num(cfg.spreadMul)},
        shimmer: ${Boolean(cfg.shimmer)},
        max_live_dur: ${num(cfg.maxLiveDur)},
        layers: &GENERATED_CHORD_${rustConstIdent(id)}_LAYERS,
    }
}`;
}

function renderLeadExtras(id, cfg) {
  const extras = pocketLeadExtraLayers(cfg);
  return `const GENERATED_LEAD_${rustConstIdent(id)}_EXTRAS: [NativeLeadExtraConfig; ${extras.length}] = [${extras.length ? `\n${extras.map(renderLeadExtra).join(",\n")}\n` : ""}];`;
}

function renderLeadExtra(extra) {
  return `    NativeLeadExtraConfig {
        freq_mul: ${num(extra.freqMul ?? 1)},
        midi_offset: ${num(extra.midiOffset ?? 0)},
        wave: "${extra.wave}",
        peak: ${num(extra.peak ?? 0)},
        filter: "${extra.filter}",
        freq: ${num(extra.freq ?? 0)},
        offset: ${num(extra.offset ?? 0)},
        dur_mul: ${num(extra.durMul ?? 1)},
        max_dur: ${optionNum(extra.maxDur)},
    }`;
}

function renderLeadInstrument(id, cfg) {
  return `fn generated_native_lead_${rustIdent(id)}() -> NativeLeadConfig {
    NativeLeadConfig {
        wave: "${cfg.wave}",
        peak: ${num(cfg.peak)},
        filter: "${cfg.filter}",
        freq: ${num(cfg.freq)},
        dur_mul: ${num(cfg.durMul)},
        extras: &GENERATED_LEAD_${rustConstIdent(id)}_EXTRAS,
    }
}`;
}

function renderGuitarTone(id, cfg) {
  return `fn generated_guitar_tone_${rustIdent(id)}() -> NativeGuitarToneConfig {
    NativeGuitarToneConfig {
        drive: ${num(cfg.drive)},
        input: ${num(cfg.input)},
        peak: ${num(cfg.peak)},
        lowpass: ${num(cfg.lowpass)},
        highpass: ${num(cfg.highpass)},
        body: ${num(cfg.body)},
        mid: ${num(cfg.mid)},
        spread: ${num(cfg.spread)},
        sustain: ${num(cfg.sustain)},
        mute: ${num(cfg.mute)},
        scratch: ${num(cfg.scratch)},
    }
}`;
}

function rustIdent(id) {
  return id.replace(/[^a-zA-Z0-9_]/g, "_");
}

function rustConstIdent(id) {
  return rustIdent(id).toUpperCase();
}

function optionNum(value) {
  return value === undefined || value === null ? "None" : `Some(${num(value)})`;
}

function num(value) {
  if (!Number.isFinite(Number(value))) throw new Error(`Invalid numeric recipe value: ${value}`);
  const text = String(Number(value));
  return text.includes(".") ? text : `${text}.0`;
}
